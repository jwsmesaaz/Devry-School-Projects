    private void btnGuessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuessActionPerformed
        // TODO add your handling code here:
        int n = Integer.parseInt(txtGuess.getText());
        int intThis = 0;
        intGuess++;
        txtGuessNumber.setText("Please enter your guess #" + intGuess + ":");
        txtBest.setText("You must make it in " + (intBest - (intGuess - 1)) + " to beat the high score");
        

        if(n<1)
        {
            txtToUser.setText("Please enter a number higher than 1");
        }
        else if(n>1000)
        {
            txtToUser.setText("Please enter a number less than 1000");
        }
        else
        {
            if(intRandom == n)
            {
                txtToUser.setText("Correct");
                intThis = 0;
                txtGuess.setEditable(false);
                txtGuessNumber.setText("Please press ''New Game'' To play again");
                
                    txtTaunt.setText("You made it in " + (intGuess - 1) + ". Try and make it in Less.");
                    
                    if(intBest == 500)
                    {
                         txtBest.setText("You made it in " + (intGuess - 1) + " This is your first time today try again");
                         intBest = intGuess - 1;
                    }
                    else if(intBest>(intGuess - 1))
                    {
                        txtBest.setText("You beat your high score for today!!! Congradulations new high " + (intGuess - 1));
                        intBest = intGuess - 1;
                    }
                    else if(intBest<(intGuess - 1))
                    {
                        txtBest.setText("Current High score is " + intBest + " Try and Beat that");
                    }
                    else if(intBest == (intGuess - 1))
                    {
                         txtBest.setText("You tied the current high score at " + intBest);
                    }
                    
                    
                

            }
            else if(n>intRandom)
            {
                intThis = n - intRandom;
                txtToUser.setText("Too High");
            }
            else
            {
                intThis = intRandom - n;
                txtToUser.setText("Too Low");
            }

            if(intThis == 0)
            {
                txtToUser.setForeground(Color.black);
            }
            else if(intThis == intLast)
            {
                txtToUser.setForeground(Color.black);
            }
            else if(intThis>intLast)
            {
                txtToUser.setForeground(Color.blue);
            }
            else
            {
                txtToUser.setForeground(Color.red);
            }

            intLast = intThis;


        }

    }//GEN-LAST:event_btnGuessActionPerformed

