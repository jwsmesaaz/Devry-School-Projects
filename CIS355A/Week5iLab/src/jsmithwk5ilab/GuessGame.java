    public int RandomNumber()
    {
        Random rNumber = new Random();
        int r = rNumber.nextInt(1000);

        while(r<1)
        {
            r = rNumber.nextInt(1000);
        }
        if(r>1000)
        {
            r = RandomNumber();
        }

        return r;
    }

